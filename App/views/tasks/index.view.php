<script>// Accordion functionality
const accordionHeaders = document.querySelectorAll('.accordion-header');

accordionHeaders.forEach(header => {
    header.addEventListener('click', function () {
        // Toggle active class on the clicked header
        this.classList.toggle('active');

        // Get the associated content panel
        const content = this.nextElementSibling;

        if (this.classList.contains('active')) {
            // Expand the content panel
            content.style.maxHeight = content.scrollHeight + 'px';
            content.style.padding = '10px 20px'; // Add padding when expanded
        } else {
            // Collapse the content panel
            content.style.maxHeight = '0';
            content.style.padding = '0 20px'; // Remove padding when collapsed
        }
    });
});
</script>






